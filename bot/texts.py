def claim_notice(email: str, license_id: int, user_id: int,
                 username: str | None) -> str:
    """Владельцу: ключ ушёл. Видно, кому именно, — на случай, если почту
    покупателя узнал кто-то другой."""
    who = f"@{escape(username)}" if username else f"id {user_id}"
    return (f"🔑 Ключ №{license_id} забрал {who}\n"
            f"Почта заказа: {escape(email)}")


def order_line(row) -> str:
    """Заказ одной карточкой для владельца.

    По ней разбирают жалобу «оплатил, ключа нет», поэтому четыре состояния
    заказа — ключ не выдан, выдан и ждёт, забран, возвращён — читаются с
    первого взгляда.
    """
    if row["refunded_at"]:
        state = "↩️ возврат"
    elif row["claimed_by"]:
        state = f"✅ ключ №{row['license_id']}, забрал {row['claimed_by']}"
    elif row["license_id"]:
        state = f"🔑 ключ №{row['license_id']}, не забран"
    else:
        state = "⏳ ключ не выдан"
    created = str(row["created"]).replace("T", " ")[:16]
    parts = [created]
    if row["amount"]:
        parts.append(escape(row["amount"]))
    parts.append(state)
    return (f"<code>{escape(row['order_id'])}</code>\n"
            + " · ".join(parts))


def sale_notice(email: str, product: str, amount: str | None,
                order_id: str | None) -> str:
    """Владельцу: пришла оплата. Заодно это единственный признак, что вебхук
    lava.top доходит и разбирается."""
    lines = [f"💰 <b>Продажа</b> — {escape(product)}",
             f"Почта: {escape(email)}"]
    if amount:
        lines.append(f"Сумма: {escape(amount)}")
    if order_id:
        lines.append(f"Заказ: <code>{escape(order_id)}</code>")
    lines.append("\nКлюч покупатель заберёт сам, прислав почту боту.")
    return "\n".join(lines)


def refund_notice(email: str, product: str, license_id: int | None) -> str:
    """Владельцу: деньги вернули.

    Приложение проверяет ключи офлайн, поэтому отозвать выданный можно только
    руками — номер вносится в `revoked` и уезжает с ближайшим обновлением.
    """
    head = (f"↩️ <b>Возврат</b> — {escape(product)}\n"
            f"Почта: {escape(email)}")
    if license_id is None:
        return head + "\n\nКлюч по этому заказу не выдавался — делать ничего не нужно."
    return (
        f"{head}\n"
        f"Выдан ключ №{license_id}.\n\n"
        f"Внесите {license_id} в <code>revoked</code> "
        "(app/lib/services/license_service.dart) и выпустите обновление — "
        "иначе ключ продолжит работать."
    )
