def escape(value) -> str:
    """Текст, безопасный внутри HTML-сообщения телеграма."""
    if value is None:
        return ""
    return (str(value).replace("&", "&amp;")
                      .replace("<", "&lt;")
                      .replace(">", "&gt;"))


